#У Python є кілька типів розгалужень для управління потоком виконання програми:

### 1. Оператор `if`

#Виконується, якщо умова істинна:

x = 10
if x > 5:
    print("x більше за 5")

### 2. Оператор `if-else`

#Якщо умова істинна, виконується блок `if`, якщо хибна — блок `else`:

x = 3
if x > 5:
    print("x більше за 5")
else:
    print("x менше або дорівнює 5")

### 3. **Оператор `if-elif-else`**

#Використовується для перевірки кількох умов:

x = 7
if x > 10:
    print("x більше за 10")
elif x == 7:
    print("x дорівнює 7")
else:
    print("x менше за 10 і не дорівнює 7")

### 4. **Вкладені розгалуження**

#Умови можуть бути вкладені одна в одну:

x = 15
if x > 10:
    if x < 20:
        print("x між 10 і 20")
    else:
        print("x більше або дорівнює 20")
else:
    print("x менше або дорівнює 10")

### 5. **Тернарний оператор**

#Коротка форма для `if-else` в один рядок:

x = 10
result = "парне" if x % 2 == 0 else "непарне"
print(result)

### 6. **Оператор `match-case`** (Python 3.10+)

#Подібно до `switch` в інших мовах програмування:

day = "Monday"

match day:
    case "Monday":
        print("Понеділок")
    case "Tuesday":
        print("Вівторок")
    case _:
        print("Невідомий день")

#Ці розгалуження охоплюють всі можливі способи керування логікою в Python.