# Приклад циклу for
letters = ["a", "b", "c"]
for i in range(len(letters)):
    print(f"На позиції {i} знаходиться буква {letters[i]}")

# Приклад циклу while
counter = 0
while counter < 3:
    print(f"Це цикл while: {counter}")
    counter += 1

#Ось ще кілька прикладів кодів, які демонструють роботу циклів у Python:

### 1. Цикл `for`
#Цей цикл перебирає елементи списку та друкує їх.
fruits = ["яблуко", "банан", "вишня"]
for fruit in fruits:
    print(fruit)

### 2. Цикл `while`
#Цей цикл продовжує виконуватись, поки змінна `count` менша за 5.

count = 0
while count < 5:
    print("Це номер:", count)
    count += 1

### 3. Використання циклу `for` з `range()`
#Цей цикл друкує числа від 0 до 4.

for i in range(5):
    print("Число:", i)

