# Колекції: списки
l = [1, "1", 2, "2", "Два", [1, "Один"]]
print(l[0], l[3], l[-1])  # 1 2 [1, 'Один']

l.append(6)  # додавання елемента в кінець списку
l.insert(0, "Початок")  # додавання на конкретну позицію
l.remove("2")  # видалення першого входження елемента
l.pop(3)  # видалення елемента за індексом
print(l)  # ['Початок', 1, '1', 'Два', [1, 'Один'], 6]


