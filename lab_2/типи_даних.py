# Кортежі
t = (2, "2", 6, 8, 2, 6)
print(t[0], "Кількість двійок:", t.count(2))  # 2 Кількість двійок: 2

# Перетворення кортежа в список та модифікація
l = list(t)
l.pop(0)  # видаляємо перший елемент
print("А тепер кількість 2:", l.count(2))  # 1

# Множини (унікальні елементи)
s = set(t)
print(s)  # {8, 2, 6, '2'}
s.remove(6)  # видаляємо елемент
print(s)  # {8, 2, '2'}
